// Helper: returns the appropriate icon component for a social link name
export function getSocialIcon(name: string, className?: string) {
  const n = name.toLowerCase();
  if (n.includes("linkedin")) return <IconLinkedIn className={className} />;
  if (n.includes("github")) return <IconGitHub className={className} />;
  if (n.includes("facebook")) return <IconFacebook className={className} />;
  if (n.includes("twitter") || n.includes("x.com")) return <IconTwitter className={className} />;
  if (n.includes("youtube")) return <IconYouTube className={className} />;
  if (n.includes("instagram")) return <IconInstagram className={className} />;
  return <IconGlobe className={className} />;
}
